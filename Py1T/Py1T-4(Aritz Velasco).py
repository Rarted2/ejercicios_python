# Pedir el nombre completo al usuario
nombre_completo = input("Introduce tu nombre completo: ").strip()

# Mostrar el nombre en minúsculas
print("En minúsculas:", nombre_completo.lower())

# Mostrar el nombre en mayúsculas
print("En mayúsculas:", nombre_completo.upper())

# Mostrar el nombre con la primera letra de cada palabra en mayúscula
print("Capitalizado:", nombre_completo.title())
